def check_vt_ip(ip, api_key):
    """Проверяет IP через VirusTotal API."""
    if not api_key:
        return "пропущено"

    url = f"https://www.virustotal.com/api/v3/ip_addresses/{ip}"
    headers = {"x-apikey": api_key}

    try:
        r = requests.get(url, headers=headers, timeout=10)
        if r.status_code == 200:
            stats = r.json()["data"]["attributes"]["last_analysis_stats"]
            malicious = stats.get("malicious", 0)
            return "malicious" if malicious > 0 else "clean"
        else:
            return f"err {r.status_code}"
    except Exception:
        return "err"


def send_telegram_alert(ip, count, vt_status, now):
    """Отправляет уведомление об угрозе в Telegram."""
    if not TELEGRAM_BOT_TOKEN or not TELEGRAM_CHAT_ID:
        return False

    url = f"https://api.telegram.org/bot{TELEGRAM_BOT_TOKEN}/sendMessage"
    text = f"""УГРОЗА ОБНАРУЖЕНА

IP: {ip}
Алертов: {count}
VirusTotal: {vt_status}
Время: {now:%Y-%m-%d %H:%M:%S} MSK

IP заблокирован (имитация)"""

    payload = {
        "chat_id": TELEGRAM_CHAT_ID,
        "text": text,
        "parse_mode": "HTML"
    }

    try:
        r = requests.post(url, data=payload, timeout=8)
        if r.status_code == 200:
            return True
        else:
            print(f"Telegram ошибка: {r.status_code}")
            return False
    except Exception as e:
        print(f"Telegram исключение: {e}")
        return False


def main():
    msk = pytz.timezone("Europe/Moscow")
    now = datetime.now(msk)
    print(f"\nАнализ угроз — {now:%Y-%m-%d %H:%M:%S} MSK")
    print("═" * 70)

    vt_key = get_vt_api_key()
    global TELEGRAM_BOT_TOKEN, TELEGRAM_CHAT_ID
    TELEGRAM_BOT_TOKEN, TELEGRAM_CHAT_ID = get_telegram_credentials()

    df = load_alerts()
    if df.empty:
        print("Нет данных для анализа")
        return

    agg = df.groupby("src_ip").size().reset_index(name="alert_count")
    agg = agg.sort_values("alert_count", ascending=False)

    results = []

    for _, row in agg.iterrows():
        ip = row["src_ip"]
        cnt = int(row["alert_count"])

        vt = check_vt_ip(ip, vt_key)

        threat = (cnt >= ALERT_THRESHOLD) or (vt == "malicious")

        print(f"  {ip:16}   алертов: {cnt:3}   VT: {vt:10}   {'УГРОЗА' if threat else 'нормально'}")

        if threat:
            print(f"     → имитация блокировки IP {ip}")
            if send_telegram_alert(ip, cnt, vt, now):
                print("     → уведомление отправлено в Telegram")
            else:
                print("     → Telegram не настроен / ошибка отправки")

        results.append({
            "ip": ip,
            "alert_count": cnt,
            "vt_status": vt,
            "threat": "YES" if threat else "нет"
        })

    df_out = pd.DataFrame(results)
    df_out.to_csv(REPORT_CSV, index=False, encoding="utf-8-sig")
    df_out.to_json(REPORT_JSON, orient="records", force_ascii=False, indent=2)

    print(f"\nСохранено:")
    print(f"  • {REPORT_CSV}")
    print(f"  • {REPORT_JSON}")

    if not df_out.empty:
        plt.figure(figsize=(10, 5))
        top = df_out.head(10)
        colors = ["#c62828" if t == "YES" else "#ff9800" for t in top["threat"]]

        plt.bar(top["ip"], top["alert_count"], color=colors)
        plt.title("Топ IP по количеству алертов")
        plt.ylabel("Количество алертов")
        plt.xlabel("IP")
        plt.xticks(rotation=45, ha="right")
        plt.tight_layout()
        plt.savefig(GRAPH_FILE, dpi=120, bbox_inches="tight")
        plt.close()

        print(f"График сохранён: {GRAPH_FILE}")

    print("═" * 70)


if __name__ == "__main__":
    main()
