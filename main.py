def load_alerts():
    """Загружает события alert из eve.json или создаёт демонстрационные данные."""
    if os.path.exists(LOG_FILE):
        print(f"Используется лог-файл: {LOG_FILE}")
        path = LOG_FILE
    else:
        print(f"Файл {LOG_FILE} не найден.")
        answer = input("Запустить в демонстрационном режиме? (да/нет): ").strip().lower()

        if answer not in ("да", "д", "y", "yes"):
            print("Работа завершена.")
            sys.exit(0)

        # При положительном ответе создаём демо-лог
        create_demo_logs()
        path = DEMO_LOG_FILE
        print(f"Демонстрационный режим: {DEMO_LOG_FILE}")

    events = []
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            try:
                event = json.loads(line)
                if event.get("event_type") == "alert":
                    src_ip = event.get("src_ip")
                    alert = event.get("alert", {})
                    events.append({
                        "src_ip": src_ip,
                        "severity": alert.get("severity"),
                        "signature": alert.get("signature", "—")
                    })
            except json.JSONDecodeError:
                continue

    df = pd.DataFrame(events)
    if df.empty:
        print("В файле нет событий типа alert")
    return df


def get_vt_api_key():
    """Возвращает ключ VirusTotal или пустую строку при пропуске."""
    key = os.getenv("VT_API_KEY")

    if key and key.strip():
        print(f"VirusTotal API-ключ найден (длина {len(key)} символов)")
        return key

    print("\nVirusTotal API-ключ не найден.")
    print("Введите ключ или нажмите Enter для работы без VirusTotal")
    key = getpass.getpass("VirusTotal API-ключ: ").strip()

    if key:
        print("Ключ принят")
    else:
        print("Продолжаем без VirusTotal")

    return key


